import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.resolve(SCRIPT_DIR, "..");
const DEFAULT_SVG_DIR = path.join(ROOT_DIR, "svg");
const DEFAULT_OUTPUT_FILE = path.join(ROOT_DIR, "src", "generatedDisclaimerAssets.ts");

function parseDimension(svg, attrName) {
  const match = svg.match(new RegExp(`${attrName}=["']([0-9.]+)(?:px)?["']`, "i"));
  return match ? Number(match[1]) : null;
}

function parseViewBoxDimension(svg, index) {
  const match = svg.match(/viewBox=["']\s*[-0-9.]+\s+[-0-9.]+\s+([-0-9.]+)\s+([-0-9.]+)\s*["']/i);
  return match ? Number(match[index]) : null;
}

function readSvgDimensions(svg, filename) {
  const width = parseDimension(svg, "width") ?? parseViewBoxDimension(svg, 1);
  const height = parseDimension(svg, "height") ?? parseViewBoxDimension(svg, 2);

  if (!Number.isFinite(width) || !Number.isFinite(height) || width <= 0 || height <= 0) {
    throw new Error(`Cannot read positive width/height from ${filename}`);
  }

  return { width, height };
}

function isWhitePaint(value) {
  return /^(?:white|#fff|#ffffff)$/i.test(value.trim());
}

function addOrReplaceFillOpacity(tag) {
  if (/\sfill-opacity=(["'])[^"']*\1/i.test(tag)) {
    return tag.replace(/\sfill-opacity=(["'])[^"']*\1/i, ' fill-opacity="0.3"');
  }

  return tag.replace(/\/?>$/, (ending) => ` fill-opacity="0.3"${ending}`);
}

function normalizeStylePaint(style) {
  const hasWhiteFill = /(?:^|;)\s*fill\s*:\s*(?:white|#fff|#ffffff)\s*(?=;|$)/i.test(
    style
  );

  if (!hasWhiteFill) return style;

  let normalized = style.replace(
    /(^|;)(\s*fill\s*:\s*)(white|#fff|#ffffff)(\s*)(?=;|$)/gi,
    "$1$2black$4"
  );

  if (/(^|;)\s*fill-opacity\s*:/i.test(normalized)) {
    normalized = normalized.replace(
      /(^|;)(\s*fill-opacity\s*:\s*)[^;]+/gi,
      "$1$20.3"
    );
  } else {
    const separator = normalized.trim().endsWith(";") ? "" : ";";
    normalized = `${normalized}${separator}fill-opacity:0.3;`;
  }

  return normalized;
}

export function normalizeSvgPaint(svg) {
  return svg
    .replace(/<[^>]+\sfill=(["'])([^"']+)\1[^>]*>/gi, (tag, _quote, fill) => {
      if (!isWhitePaint(fill)) return tag;

      const normalizedTag = tag.replace(
        /\sfill=(["'])(?:white|#fff|#ffffff)\1/i,
        ' fill="black"'
      );

      return addOrReplaceFillOpacity(normalizedTag);
    })
    .replace(/\sstyle=(["'])(.*?)\1/gi, (attribute, quote, style) => {
      const normalized = normalizeStylePaint(style);
      return normalized === style ? attribute : ` style=${quote}${normalized}${quote}`;
    });
}

export async function readSvgAssets(svgDir = DEFAULT_SVG_DIR) {
  const entries = await readdir(svgDir, { withFileTypes: true });
  const svgFiles = entries
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith(".svg"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b, "ru"));

  const assets = [];

  for (const filename of svgFiles) {
    const rawSvg = await readFile(path.join(svgDir, filename), "utf8");
    const svg = normalizeSvgPaint(rawSvg);
    const key = path.basename(filename, ".svg");
    const { width, height } = readSvgDimensions(svg, filename);

    assets.push({
      key,
      label: key,
      filename,
      svg,
      width,
      height,
    });
  }

  return assets;
}

export function buildAssetModule(assets) {
  const entries = assets
    .map((asset) => {
      return `  ${JSON.stringify(asset.key)}: {
    key: ${JSON.stringify(asset.key)},
    label: ${JSON.stringify(asset.label)},
    filename: ${JSON.stringify(asset.filename)},
    svg: ${JSON.stringify(asset.svg)},
    width: ${asset.width},
    height: ${asset.height},
  },`;
    })
    .join("\n");
  const assetList = assets
    .map((asset) => `  DISCLAIMER_ASSETS[${JSON.stringify(asset.key)}],`)
    .join("\n");

  return `// This file is generated by scripts/generate-disclaimer-assets.mjs.
// Do not edit it by hand. Edit SVG files in /svg, then run npm run build.

export interface DisclaimerAsset {
  key: string;
  label: string;
  filename: string;
  svg: string;
  width: number;
  height: number;
}

export const DISCLAIMER_ASSETS: Record<string, DisclaimerAsset> = {
${entries}
};

export const DISCLAIMER_ASSET_LIST: DisclaimerAsset[] = [
${assetList}
];
`;
}

export async function generateDisclaimerAssets({
  svgDir = DEFAULT_SVG_DIR,
  outputFile = DEFAULT_OUTPUT_FILE,
} = {}) {
  const assets = await readSvgAssets(svgDir);
  const moduleText = buildAssetModule(assets);

  await mkdir(path.dirname(outputFile), { recursive: true });
  await writeFile(outputFile, moduleText, "utf8");

  return { assets, outputFile };
}

const isCliRun = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isCliRun) {
  const { assets, outputFile } = await generateDisclaimerAssets();
  console.log(`Generated ${path.relative(ROOT_DIR, outputFile)} with ${assets.length} SVG assets.`);
}
